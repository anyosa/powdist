#' Reversal power logistic distribution
#'
#' Density function for the reversal power logistic distribution with parameters mu, sigma and lambda.
#' @param x vector of quantiles.
#' @param lambda shape parameter.
#' @param mu,sigma location and scale parameters.
#' @references Bazán, J. L., Torres‐Avilés, F., Suzuki, A. K., & Louzada, F. (2017). Power and reversal power links for binary regressions: An application for motor insurance policyholders. Applied Stochastic Models in Business and Industry, 33(1), 22-34.
#' @examples
#' drpl(1, 1, 3, 4)
drpl <- function(x, lambda, mu = 0, sigma = 1){
  d = (lambda/sigma) * dlogis((x-mu)/sigma) * (plogis((-x-mu)/sigma)**(lambda-1))
  return(d)
}

#' Reversal power logistic distribution
#'
#' Distribution function for the reversal power logistic distribution with parameters mu, sigma and lambda.
#' @param q vector of quantiles.
#' @param lambda shape parameter.
#' @param mu,sigma location and scale parameters.
#' @references Bazán, J. L., Torres‐Avilés, F., Suzuki, A. K., & Louzada, F. (2017). Power and reversal power links for binary regressions: An application for motor insurance policyholders. Applied Stochastic Models in Business and Industry, 33(1), 22-34.
#' @examples
#' prpl(1, 1, 3, 4)
prpl <- function(q, lambda, mu = 0, sigma = 1){
  p = plogis(-q, mu, sigma)
  return(1-p**lambda)
}

#' Reversal power logistic distribution
#'
#' Quantile function for the reversal power logistic distribution with parameters mu, sigma and lambda.
#' @param p vector of probabilities.
#' @param lambda shape parameter.
#' @param mu,sigma location and scale parameters.
#' @references Bazán, J. L., Torres‐Avilés, F., Suzuki, A. K., & Louzada, F. (2017). Power and reversal power links for binary regressions: An application for motor insurance policyholders. Applied Stochastic Models in Business and Industry, 33(1), 22-34.
#' @examples
#' qrpl(0.2, 1, 3, 4)
qrpl <- function(p, lambda, mu = 0, sigma = 1){
  q = -qlogis((1-p)**(1/lambda))* sigma + mu
  return(q)
}

#' Reversal power logistic distribution
#'
#' Random generation for the reversal power logistic distribution with parameters mu, sigma and lambda.
#' @param n number of observations. If length(n) > 1, the length is taken to be the number required.
#' @param lambda shape parameter.
#' @param mu,sigma location and scale parameters.
#' @references Bazán, J. L., Torres‐Avilés, F., Suzuki, A. K., & Louzada, F. (2017). Power and reversal power links for binary regressions: An application for motor insurance policyholders. Applied Stochastic Models in Business and Industry, 33(1), 22-34.
#' @examples
#' rrpl(5, 2, 3, 4)
rrpl = function(n, lambda, mu= 0, sigma = 1){
  n = runif(n)
  x = qrpl(n, lambda, mu, sigma)
  return(x)
}
